# Description : Creates, validates, and optionally publishes a UH MDM /
#               WiFi device certificate template in AD CS using the
#               supported CertEnroll COM template authoring path
#               (IX509CertificateTemplateWritable) together with the
#               PSPKI module. The template is cloned from a known-good
