function Grant-TemplateEnrollmentRights
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory)]
        [string]$ShortName,

        [Parameter(Mandatory)]
        [string]$Principal,

        [Parameter(Mandatory)]
        [bool]$AddAutoEnroll
    )

    # PSPKI ACL cmdlet parameter names can vary by module version; the
    # pipeline idiom below is the documented form. VERIFY against your build.
    $template = Get-CertificateTemplate -Name $ShortName

    if ($AddAutoEnroll)
    {
        $acl = $template |
            Get-CertificateTemplateAcl |
            Add-CertificateTemplateAcl -User $Principal -AccessType Allow -AccessMask Read, Enroll, Autoenroll
    }
    else
    {
        $acl = $template |
            Get-CertificateTemplateAcl |
            Add-CertificateTemplateAcl -User $Principal -AccessType Allow -AccessMask Read, Enroll
    }

    $acl | Set-CertificateTemplateAcl | Out-Null
}

function Publish-TemplateToCA
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory)]
        [string]$ShortName
    )

    $caHost = ($CAConfig -split '\\')[0]

    Write-InfoMsg -Message ('Connecting to issuing CA [{0}].' -f $caHost)
    $ca = Connect-CertificationAuthority -ComputerName $caHost

    $alreadyPublished = Get-CATemplate -CertificationAuthority $ca |
        Where-Object { $_.Name -eq $ShortName }

    if ($alreadyPublished)
    {
        Write-SuccessMsg -Message ('Template [{0}] is already published on [{1}].' -f $ShortName, $caHost)
        Add-LedgerEntry -Action 'Publish to CA' -Result 'Already published' -Detail $ShortName
        return
    }

    Get-CertificateTemplate -Name $ShortName |
        Add-CATemplate -CertificationAuthority $ca

    Write-SuccessMsg -Message ('Published template [{0}] to [{1}].' -f $ShortName, $caHost)
    Add-LedgerEntry -Action 'Publish to CA' -Result 'Published' -Detail $ShortName
}

