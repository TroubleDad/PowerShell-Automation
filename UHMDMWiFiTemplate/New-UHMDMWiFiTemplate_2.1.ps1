function Get-BaseTemplateComObject
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory)]
        [string]$BaseName
    )

    # Load the machine enrollment policy from Active Directory and walk the
    # returned IX509CertificateTemplates collection. The base is matched on
    # either its common name (cn) or its friendly (display) name, because
    # built-in templates frequently differ between the two (for example, the
    # cn of 'Workstation Authentication' is 'Workstation').
    $policy = New-Object -ComObject $ComPolicyServer
    $policy.Initialize($EnrollContextAdminForceMachine)
    $policy.LoadPolicy($LoadOptionDefault)

    $templates = $policy.GetTemplates()
    $count     = $templates.Count

    for ($i = 0; $i -lt $count; $i++)
    {
        $candidate = $templates.ItemByIndex($i)

        $cn = $candidate.Property($TemplateProp.CommonName)
        $fn = $candidate.Property($TemplateProp.FriendlyName)

        if (($cn -eq $BaseName) -or ($fn -eq $BaseName))
        {
            return $candidate
        }
    }

    throw ('Base template [{0}] was not found in the Active Directory enrollment policy.' -f $BaseName)
}

function New-EkuObjectIds
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory)]
        [string[]]$OidValues
    )

    $collection = New-Object -ComObject $ComObjectIds

    foreach ($value in $OidValues)
    {
        $oid = New-Object -ComObject $ComObjectId
        $oid.InitializeFromValue($value)
        $collection.Add($oid)
    }

    return $collection
}

function New-MdmWiFiTemplate
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory)]
        [string]$BaseName,

        [Parameter(Mandatory)]
        [string]$ShortName,

        [Parameter(Mandatory)]
        [string]$DisplayName,

        [Parameter(Mandatory)]
        [bool]$IncludeServerAuth
    )

    if ($DryRun)
    {
        Write-DryRunMsg -Message ('Would clone base [{0}] into new template [{1}] (display ''{2}'').' -f $BaseName, $ShortName, $DisplayName)
        Write-DryRunMsg -Message 'Would set subject name to supply-in-request and commit with platform-generated OID.'

        if ($IncludeServerAuth)
        {
            Write-DryRunMsg -Message 'Would set EKUs to Client + Server Authentication.'
        }
        else
        {
            Write-DryRunMsg -Message 'Would inherit Client Authentication EKU from the base.'
        }

        Add-LedgerEntry -Action 'Clone + commit template' -Result 'DryRun' -Detail $ShortName
        return $null
    }

    Write-InfoMsg -Message ('Loading base template [{0}] from the AD enrollment policy.' -f $BaseName)
    $baseCom = Get-BaseTemplateComObject -BaseName $BaseName

    Write-InfoMsg -Message 'Initializing a writable template from the base.'
    $writable = New-Object -ComObject $ComWritableTmpl
    $writable.Initialize($baseCom)

    # Identity overrides.
    $writable.Property($TemplateProp.CommonName)   = $ShortName
    $writable.Property($TemplateProp.FriendlyName) = $DisplayName

    # Subject is supplied in the request (MDM provides subject and SAN).
    $writable.Property($TemplateProp.SubjectNameFlags) = $SubjectSupplyInRequest

    # EKUs: inherit Client Authentication from the base unless Server
    # Authentication is also requested, in which case set both explicitly.
    if ($IncludeServerAuth)
    {
        $ekuCollection = New-EkuObjectIds -OidValues @($EkuClientAuth, $EkuServerAuth)
        $writable.Property($TemplateProp.EKUs) = $ekuCollection
        Write-InfoMsg -Message 'EKUs set to Client + Server Authentication.'
    }
    else
    {
        Write-InfoMsg -Message 'EKU inherited from base (Client Authentication).'
    }

    Write-InfoMsg -Message 'Committing the new template to Active Directory (platform-generated OID).'
    $writable.Commit($CommitFlagGenerateOID, $null)

    Write-SuccessMsg -Message ('Committed template [{0}].' -f $ShortName)
    Add-LedgerEntry -Action 'Clone + commit template' -Result 'Created' -Detail ('{0} from {1}' -f $ShortName, $BaseName)

    # Allow AD replication / policy cache a moment, then verify via PSPKI.
    Start-Sleep -Seconds 3
    $newTemplate = Get-CertificateTemplate -Name $ShortName -ErrorAction SilentlyContinue

    if (-not $newTemplate)
    {
        Start-Sleep -Seconds 5
        $newTemplate = Get-CertificateTemplate -Name $ShortName -ErrorAction SilentlyContinue
    }

    if (-not $newTemplate)
    {
        throw ('Template [{0}] was not found via PSPKI after commit.' -f $ShortName)
    }

    Write-SuccessMsg -Message 'Verified the new template is present in AD DS.'
    return $newTemplate
}

